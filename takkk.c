#include <xc.h>

// CONFIG settings here or use config bits window

#define _XTAL_FREQ 4000000

// Define shift register control pins
#define DS    LATCbits.LATC1
#define SHCP  LATCbits.LATC0
#define STCP  LATCbits.LATC2

// Define digit control (transistors)
#define DIG1  LATDbits.LATD0
#define DIG2  LATDbits.LATD1

// Segment encoding for digits 0?9 (common cathode)
const unsigned char segment_map[10] = {
    0x3F, // 0
    0x06, // 1
    0x5B, // 2
    0x4F, // 3
    0x66, // 4
    0x6D, // 5
    0x7D, // 6
    0x07, // 7
    0x7F, // 8
    0x6F  // 9
};

// Function to send a byte to 74HC595
void sendByte(unsigned char data) {
    for (char i = 0; i < 8; i++) {
        SHCP = 0;
        DS = (data & 0x80) ? 1 : 0;  // MSB first
        SHCP = 1;
        data <<= 1;
    }

    STCP = 0;
    STCP = 1;
}

// Initialization
void init(void) {
    TRISC = 0xF8; // RC0?2 as output (shift reg)
    LATC = 0x00;

    TRISD = 0xFC; // RD0, RD1 as output
    LATD = 0x00;
}

// Main loop
void main(void) {
    unsigned char digit1 = 5; // Display 1 will show 5
    unsigned char digit2 = 8; // Display 2 will show 8

    init();

    while (1) {
        // Display digit 1
        sendByte(segment_map[digit1]);
        DIG1 = 1;
        __delay_ms(1000);
        DIG1 = 0;

        // Display digit 2
        sendByte(segment_map[digit2]);
        DIG2 = 1;
        __delay_ms(1000);
        DIG2 = 0;
    }
}



